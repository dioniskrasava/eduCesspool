interface Car{

    val model: Model
    val engine: Engine

    interface Engine{
        fun start()
        fun stop()
    }

    data class Model (
        val name: String,
        val year: Int
    )

}

class Toyota(
    override val model: Car.Model,
    override val engine: Car.Engine
) : Car

// –ø—Ä–∏–º–µ—Ä –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –≤—ã—à–µ–æ–ø–∏—Å–∞–Ω–Ω—ã—Ö –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–æ–≤ –∏ –∫–ª–∞—Å—Å–æ–≤
