    // –∑–∞–±–µ–≥–∞–µ–º –Ω–µ–º–Ω–æ–≥–æ –≤–ø–µ—Ä–µ–¥ (—Å–ø–∞—Å–∏–±–æ, DeepSeek)
    val engine = object : Car.Engine {
        override fun start() {
            println("–î–≤–∏–≥–∞—Ç–µ–ª—å –∑–∞–ø—É—Å—Ç–∏–ª—Å—è!")
        }

        override fun stop() {
            println("–î–≤–∏–≥–∞—Ç–µ–ª—å –æ—Å—Ç–∞–Ω–æ–≤–∏–ª—Å—è!")
        }
    }


    // –û–ë–™–ï–ö–¢ –ò–ù–§–û–†–ú–ê–¶–ò–ò –û –ú–û–î–ï–õ–ò
    val model = Car.Model("Camry", 2022)

