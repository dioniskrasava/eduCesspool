interface Animal {
    // –≤–ª–æ–∂–µ–Ω–Ω—ã–π –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å –¥–ª—è —Å–ø–æ—Å–æ–±–Ω–æ—Å—Ç–∏ –∏–∑–¥–∞–≤–∞—Ç—å –∑–≤—É–∫–∏
